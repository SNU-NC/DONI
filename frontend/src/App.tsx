const InputComponent = memo(({ onSend, isThinking }: { onSend: (value: string) => void, isThinking: boolean }) => {
    const [inputValue, setInputValue] = useState('');

    const handleSend = useCallback(() => {
        if (!inputValue.trim()) return;
        onSend(inputValue);
        setInputValue('');
    }, [inputValue, onSend]);

    return (
        <div className="input-container">
            <input
                type="text"
                value={inputValue}
                onChange={(e) => setInputValue(e.target.value)}
                onKeyPress={(e) => e.key === 'Enter' && handleSend()}
                placeholder="ÏßàÎ¨∏ÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî..."
                disabled={isThinking}
            />
            <button onClick={handleSend} disabled={isThinking}>
                {isThinking ? 'Ï†ÑÏÜ°' : 'Ï†ÑÏÜ°'}
            </button>
        </div>
    );
});

function App() {
    const [messages, setMessages] = useState<Message[]>([{
        content: "ÏïàÎÖïÌïòÏÑ∏Ïöî! Í∏àÏúµ Ï†ïÎ≥¥ Í≤ÄÏÉâ AI Ï±óÎ¥áÏûÖÎãàÎã§. Í∂ÅÍ∏àÌïòÏã† ÎÇ¥Ïö©ÏùÑ ÏûêÏú†Î°≠Í≤å ÏßàÎ¨∏Ìï¥ Ï£ºÏÑ∏Ïöî.",
        isUser: false,
        timestamp: new Date().toLocaleTimeString()
    }]);
    const [references, setReferences] = useState<Reference[]>([]);
    const [isThinking, setIsThinking] = useState(false);
    const bookRef = useRef<any>();

    const handleSendMessage = useCallback(async (inputValue: string) => {
        const timestamp = new Date().toLocaleTimeString('ko-KR');
        const newMessage: Message = {
            content: inputValue,
            isUser: true,
            timestamp
        };

        setMessages(prev => [...prev, newMessage]);
        setIsThinking(true);
        
        try {
            const response = await fetch('http://localhost:8000/api/search', {
                method: 'POST',
                headers: { 
                    'Content-Type': 'application/json',
                    'Accept': 'application/json'
                },
                body: JSON.stringify({ query: inputValue })
            });

            if (!response.ok) {
                throw new Error(`HTTP error! status: ${response.status}`);
            }

            const data = await response.json();
            
            setIsThinking(false);
            
            if (data.error) {
                setMessages(prev => [...prev, {
                    content: data.message,
                    isUser: false,
                    timestamp: new Date().toLocaleTimeString('ko-KR')
                }]);
            } else {
                setMessages(prev => [...prev, {
                    content: data.answer,
                    isUser: false,
                    timestamp: new Date().toLocaleTimeString('ko-KR')
                }]);
                
                if (data.docs && data.docs.length > 0) {
                    setReferences(data.docs);
                    setTimeout(() => {
                        bookRef.current?.pageFlip().flipNext();
                    }, 1000);
                }
            }
        } catch (error) {
            console.error('API ÏöîÏ≤≠ Ïò§Î•ò:', error);
            setIsThinking(false);
            setMessages(prev => [...prev, {
                content: "Ï£ÑÏÜ°Ìï©ÎãàÎã§. ÏÑúÎ≤Ñ Ïó∞Í≤∞Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§. Ïû†Ïãú ÌõÑ Îã§Ïãú ÏãúÎèÑÌï¥ Ï£ºÏÑ∏Ïöî.",
                isUser: false,
                timestamp: new Date().toLocaleTimeString('ko-KR')
            }]);
        }
    }, []);

    return (
        <div className="app-container">
            <HTMLFlipBook
                width={600}
                height={800}
                size="stretch"
                minWidth={300}
                maxWidth={1000}
                minHeight={400}
                maxHeight={1000}
                maxShadowOpacity={0.5}
                showCover={false}
                mobileScrollSupport={true}
                className="book"
                ref={bookRef}
                style={{}}
                startPage={0}
                drawShadow={true}
                flippingTime={1000}
                usePortrait={true}
                startZIndex={0}
                autoSize={true}
                clickEventForward={true}
                useMouseEvents={true}
                swipeDistance={30}
                showPageCorners={true}
                disableFlipByClick={false}
            >
                <ChatPage messages={messages} isThinking={isThinking} />
                <ReferencePage references={references} />
            </HTMLFlipBook>
            
            <InputComponent onSend={handleSendMessage} isThinking={isThinking} />
        </div>
    );
}

export default App; 